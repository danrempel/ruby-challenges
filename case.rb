the_weather = "cloudy"
case(the_weather)
	when "snowy" then puts "Snowy sucks. Don't do it, man!"
	when "rainy" then puts "Rain smells nice but is wet. Hit the pub and watch from inside."
	when "sunny" then puts "Aww yeah, grab some ciders and hit the park!"
	when "windy" then puts "Wind is fine, but annoying."
	else puts "You should just stay inside and watch out the window. With snacks."
end